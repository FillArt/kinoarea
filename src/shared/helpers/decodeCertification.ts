export function decodeCertification(cert: string | null | undefined): string {
    if (!cert) return 'Нет рейтинга';

    const map: Record<string, string> = {
        G: 'Для всех возрастов',
        PG: 'Рекомендуется присутствие родителей',
        'PG-13': 'Детям до 13 лет просмотр не желателен',
        R: 'До 17 лет — только с родителями',
        'NC-17': 'Только для взрослых (18+)',
        '18': 'Только для взрослых (18+)',
        '16': 'С 16 лет',
        '12': 'С 12 лет',
        '6': 'С 6 лет',
        '0': 'Для всех возрастов',
        'U': 'Универсально — для всех',
        'UA': 'С родителями',
        'A': 'Взрослая аудитория',
        'AL': 'Для всех возрастов (Нидерланды)',
        'FSK 0': 'Для всех (Германия)',
        'FSK 6': 'С 6 лет (Германия)',
        'FSK 12': 'С 12 лет (Германия)',
        'FSK 16': 'С 16 лет (Германия)',
        'FSK 18': 'С 18 лет (Германия)',
    };

    return map[cert] || `Неизвестный рейтинг: ${cert}`;
}
